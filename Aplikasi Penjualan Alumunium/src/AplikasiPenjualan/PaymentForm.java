    private void jenisComboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jenisComboboxActionPerformed
        switch(jenisCombobox.getSelectedIndex()){
            case 0:
                priceBarangTxt.setText("");
                ongkirTxt.setText("");
                totalTxt.setText("");
                alamatButtonGroup.clearSelection();
                break;
                
            case 1:
                priceBarangTxt.setText("1600000");
                ongkirTxt.setText("");
                totalTxt.setText("");
                alamatButtonGroup.clearSelection();
                break;
            
            case 2:
                priceBarangTxt.setText("500000");
                ongkirTxt.setText("");
                totalTxt.setText("");
                alamatButtonGroup.clearSelection();
                break;
                
            case 3:
                priceBarangTxt.setText("1500000");
                ongkirTxt.setText("");
                totalTxt.setText("");
                alamatButtonGroup.clearSelection();
                break;
                
            default:
                priceBarangTxt.setText("650000");
                ongkirTxt.setText("");
                totalTxt.setText("");
                alamatButtonGroup.clearSelection();
                break;   
        }
    }//GEN-LAST:event_jenisComboboxActionPerformed

