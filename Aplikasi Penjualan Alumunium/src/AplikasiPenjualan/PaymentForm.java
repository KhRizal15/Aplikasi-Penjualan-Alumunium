    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed
       if(orderNameTxt.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Isian belum lengkap");
       }else if(priceBarangTxt.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "pilih jenis barang");
       }else if(ongkirTxt.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Pilih alamat pemesan");
       }else{
        //long pricebarang, ongkir, totalbayar;
        long pricebarang = Long.parseLong(priceBarangTxt.getText()),
        ongkir = Long.parseLong(ongkirTxt.getText()),
                totalbayar;
        
        totalbayar = (pricebarang + ongkir);
        totalTxt.setText("" +totalbayar);
       }         
                
    }//GEN-LAST:event_processButtonActionPerformed


